package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int a = 99;
        int b = 104;
        int c = 97;
        int d  = 114;
        char a1 = (char) a;
        char b1 = (char) b;
        char c1 = (char) c;
        char d1 = (char) d;

        System.out.println(a1);
        System.out.println(b1);
        System.out.println(c1);
        System.out.println(d1);
    }
}
